package com.stm.pegelhub.lib.model;

public class StationManufacturer {

    private String stationManufacturerName;
    private String stationManufacturerTyp;
    private String stationManufacturerFirmwareVersion;
    private String stationRemark;

    public String getStationManufacturerName() {
        return stationManufacturerName;
    }

    public void setStationManufacturerName(String stationManufacturerName) {
        this.stationManufacturerName = stationManufacturerName;
    }

    public String getStationManufacturerTyp() {
        return stationManufacturerTyp;
    }

    public void setStationManufacturerTyp(String stationManufacturerTyp) {
        this.stationManufacturerTyp = stationManufacturerTyp;
    }

    public String getStationManufacturerFirmwareVersion() {
        return stationManufacturerFirmwareVersion;
    }

    public void setStationManufacturerFirmwareVersion(String stationManufacturerFirmwareVersion) {
        this.stationManufacturerFirmwareVersion = stationManufacturerFirmwareVersion;
    }

    public String getStationRemark() {
        return stationRemark;
    }

    public void setStationRemark(String stationRemark) {
        this.stationRemark = stationRemark;
    }
}
